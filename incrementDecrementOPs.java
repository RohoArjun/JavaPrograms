public class incrementDecrementOPs {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a++); // 10 >> 11 //10
        System.out.println(++a); // 11+1 =12 //12
        System.out.println(a--); // 12 >> 12-1//11 //12
        System.out.println(--a); // 11-1>>10 //10

    }
}
